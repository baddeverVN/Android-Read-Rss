public class Cate {
    private int image;
    private String nameCate;

    public Cate(int image, String nameCate) {
        this.image = image;
        this.nameCate = nameCate;
    }

    public int getImage() {
        return image;
    }

    public String getNameCate() {
        return nameCate;
    }
}
